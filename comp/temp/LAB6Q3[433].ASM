DATA SEGMENT
NUM DW 05,04
RES DW 2 DUP(0)
DATA ENDS

CODE SEGMENT
	ASSUME CS:CODE,DS:DATA
START: 
	MOV AX,DATA
  	MOV DS,AX
	CALL LCM
	MOV AH,4CH
	INT 21H
LCM:
	MOV DX,0H
  	MOV AX,NUM 
  	MOV BX,NUM+2
UP: 
	PUSH AX
  	PUSH DX
  	DIV BX
  	CMP DX,0
  	JE EXIT
  	POP DX
  	POP AX
  	ADD AX,NUM
  	JNC DOWN
  	INC DX
DOWN: 
	JMP UP
EXIT: 
	POP RES+2
  	POP RES
	RET
CODE ENDS
END START