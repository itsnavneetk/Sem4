DATA SEGMENT
NUM DW 05H,04H,03H,02H
RES DW 2 DUP(?)
DATA ENDS

CODE SEGMENT
	ASSUME CS:CODE,DS:DATA
START: 
	MOV AX,DATA
  	MOV DS,AX
	MOV DX,0H
  	MOV AX,NUM
  	MOV BX,NUM+2
	CALL LCM 
	MOV CX,RES
	MOV DX,0H
  	MOV AX,NUM+4
  	MOV BX,NUM+6
	CALL LCM
	MOV DX,0H
  	MOV AX,RES
  	MOV BX,CX
	CALL LCM
	MOV AH,4CH
	INT 21H
LCM:
	
UP: 
	PUSH AX
  	PUSH DX
  	DIV BX
  	CMP DX,0
  	JE EXIT
  	POP DX
  	POP AX
  	ADD AX,NUM
  	JNC DOWN
  	INC DX
DOWN: 
	JMP UP
EXIT: 
	POP RES+2
	POP RES
	RET
CODE ENDS
END START
