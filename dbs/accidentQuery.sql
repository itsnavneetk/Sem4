create database accident;
use accident;

create table person(driver_id varchar(20) primary key, name varchar(20), address varchar(20) );
CREATE TABLE CAR(REGNO VARCHAR(20), MODEL VARCHAR(20), YEAR INTEGER, PRIMARY KEY(REGNO));
CREATE TABLE ACCIDENT(REPORT_NUMBER INTEGER, ACCD_DATE DATE, LOCATION VARCHAR(20), PRIMARY KEY(REPORT_NUMBER));

CREATE TABLE OWNS(DRIVER_ID VARCHAR(20), REGNO VARCHAR(20), 
FOREIGN KEY(DRIVER_ID) REFERENCES PERSON(DRIVER_ID),
FOREIGN KEY(REGNO) REFERENCES CAR(REGNO));

CREATE TABLE PARTICIPATED(DRIVER_ID VARCHAR(20),
 REGNO VARCHAR(20),
 REPORT_NUMBER INTEGER,
 DAMAGE_AMOUNT INTEGER,
 FOREIGN KEY(DRIVER_ID) REFERENCES PERSON(DRIVER_ID),
 FOREIGN KEY(REGNO) REFERENCES CAR(REGNO),
 FOREIGN KEY(REPORT_NUMBER) REFERENCES ACCIDENT(REPORT_NUMBER));
 
